// Soal 2. Melakukan Looping Menggunakan For
// ```js
// // Problem
// // Pada tugas ini kamu diminta untuk melakukan looping dalam JavaScript dengan menggunakan syntax for.
// // Untuk membuat tantangan ini lebih menarik, kamu juga diminta untuk membuat suatu looping yang menghitung maju dan
// // menghitung mundur. Jangan lupa tampilkan di console juga judul 'LOOPING PERTAMA' dan 'LOOPING KEDUA'.
// //tampilkan output yang menunjukan kalau looping itu maju pada looping pertama dan mundur pada looping kedua

for (let loopingpertama = 1; loopingpertama < 11; loopingpertama++) {
  console.log("LOOPING PERTAMA", loopingpertama);
}
for (let loopingkedua = 10; loopingkedua > 0; loopingkedua--) {
  console.log("LOOPING KEDUA", loopingkedua);
}
